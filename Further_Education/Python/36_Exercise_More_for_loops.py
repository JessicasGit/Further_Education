list1 = [7, 36, 9, 15, 48, 46, 33, 14, 20, 38, 11, 19, 29, 42, 26, 49, 16, 50, 38, 1]
sum1 = 0

# print()
for elem1 in list1:
    sum1 += elem1
print("Sum of all numbers: \n",sum1)

input1 = int(input("What number do you want to compare with the numbers of the list? \n"))

for elem2 in list1:
    if input1 > elem2:
      print("Your number",input1,"is greater than",elem2)
    elif input1 < elem2:
      print("Your number",input1,"is smaller than",elem2)
    elif input1 == elem2:
      print("Your number",input1,"equals",elem2)
